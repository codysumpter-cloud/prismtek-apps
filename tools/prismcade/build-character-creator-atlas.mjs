#!/usr/bin/env node
import { mkdir, readFile, writeFile } from "node:fs/promises";
import { dirname, resolve } from "node:path";

const repoRoot = resolve(new URL("../..", import.meta.url).pathname);
const sourceRoot = resolve(repoRoot, "packages/prismcade-character-creator-assets/source-private");
const outputRoot = resolve(repoRoot, "packages/prismcade-character-creator-assets/generated");

const sources = {
  male: {
    id: "official-male-creator",
    file: resolve(sourceRoot, "male/Official_Male_Creator.png"),
    output: resolve(outputRoot, "male/official-male-creator.source.png")
  },
  femalePreview: {
    id: "female-64-preview-sheet",
    file: resolve(sourceRoot, "female/64 Pixel Female Characters(1).png"),
    output: resolve(outputRoot, "female/female-creator-preview.source.png")
  }
};

async function copyRuntimeSource(source) {
  const bytes = await readFile(source.file);
  await mkdir(dirname(source.output), { recursive: true });
  await writeFile(source.output, bytes);
  return {
    id: source.id,
    sourcePrivatePath: source.file.replace(repoRoot + "/", ""),
    generatedPath: source.output.replace(repoRoot + "/", ""),
    policy: "Derived/runtime Prismcade use only; do not redistribute as the original standalone asset pack."
  };
}

async function main() {
  const copied = [];
  for (const source of Object.values(sources)) {
    try {
      copied.push(await copyRuntimeSource(source));
    } catch (error) {
      copied.push({
        id: source.id,
        missing: true,
        expectedSourcePrivatePath: source.file.replace(repoRoot + "/", ""),
        message: error instanceof Error ? error.message : String(error)
      });
    }
  }

  const manifest = {
    schemaVersion: "prismcade-character-creator-generated-atlas-v1",
    generatedAt: new Date().toISOString(),
    licensePolicy: {
      commercialProductUse: true,
      rawAssetPackRedistribution: false,
      note: "Runtime files generated by this script are for Prismcade product use. Raw source packs stay private/local."
    },
    copied,
    nextSlicingTargets: [
      "male idle/front/side/back/walk frames from Official_Male_Creator.png",
      "female body bases from 64 Pixel Female Characters 1.2 Body Update",
      "female hair options from 64 Pixel Female Characters 1.1 Hair Update",
      "safe outfit overlays for public/runtime exports"
    ]
  };

  await mkdir(outputRoot, { recursive: true });
  await writeFile(resolve(outputRoot, "generated-atlas.manifest.json"), `${JSON.stringify(manifest, null, 2)}\n`);
  console.log(JSON.stringify(manifest, null, 2));
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
